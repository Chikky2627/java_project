package com.tnsif.intro;

public class Typeconversion {
	
	    public static void main(String[] args) {

	        // int to float
	        int a = 10;
	        float b = a;   
	        System.out.println("int to float: " + b);

	        // float to int
	        float x = 12.75f;
	        int y = (int) x;  
	        System.out.println("float to int: " + y);
	    }
	}


